using System;

namespace MathLib
{
    public class MathEngine
    {
        public static int Addition(int n1,int n2)
        {
            return n1+n2;
        }

        public static int Addition(int n1,int n2)
        {
            return n1-n2;
        }

        public static int Mod(int n1,int n2)
        {
            return n1%n2;
        }

    }
}





