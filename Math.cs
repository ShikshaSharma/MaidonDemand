using System;

namespace MathLib
{
    public class MathEngine
    {
        public static int Addition(int n1,int n2)
        {
            return n1+n2;
        }
        public static int Addition(int n1,int n2)
        {
            return n1-n2;
        }

        public static int multiplication(int n1, int n2)
        {
            return n1*n2;
        }
        //new and last change
        public static int divide(int n1, int n2)
        {
            return n1/n2;
        }

    }
}





